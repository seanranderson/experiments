% This is a generic function used to provide "helpful" text to the 
% participant (e.g., instructions on how to troubleshoot, contact 
% information for the experimenter). 
% 
% Sean R. Anderson - sean.hearing@gmail.com

function Help(~,~,~)
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%% open experimental screen
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%

    hndl = get(gcf, 'UserData');
    clf(hndl.ExpFig);
    hndl.npanel = hndl.npanel - 1;
    
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%% set up experimental screen
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%

    uicontrol('Style','text','String','Uh oh...', ...
        'FontWeight', 'bold', ...
        'backgroundcolor',hndl.colors.gray,...
        'FontSize',40,...
        'Units', 'normalized', 'Position',[0.1,0.85,0.8,0.075]);
    
    uicontrol('Style','text',...
        'String','Looks like there was some trouble!', ...
        'backgroundcolor',hndl.colors.gray,...
        'FontSize',30,...
        'Units', 'normalized', 'Position',[0.1,0.8,0.8,0.05]);
    
    uicontrol('Style','text',...
        'String','Please contact the experimenter using', ...
        'backgroundcolor',hndl.colors.gray,...
        'FontSize',30,...
        'Units', 'normalized', 'Position',[0.1,0.75,0.8,0.052]);
    
    uicontrol('Style','text',...
        'String','the contact information you received with this package.', ...
        'backgroundcolor',hndl.colors.gray,...
        'FontSize',30,...
        'Units', 'normalized', 'Position',[0.1,0.7,0.8,0.052]);
    
    uicontrol('Style','text',...
        'String','If you cannot find this contact information, email:', ...
        'backgroundcolor',hndl.colors.gray,...
        'FontSize',30,...
        'Units', 'normalized', 'Position',[0.1,0.6,0.8,0.052]);
    
    uicontrol('Style','text',...
        'FontWeight','Bold',...
        'String','sean.hearing@gmail.com.', ...
        'backgroundcolor',hndl.colors.gray,...
        'FontSize',30,...
        'Units', 'normalized', 'Position',[0.1,0.55,0.8,0.052]);
    
    hndl.ExpButtons.Resume = uicontrol('Style', 'pushbutton', 'String', 'Resume', ...
        'FontWeight', 'bold', ...
        'FontSize', 8, ...
        'FontUnits','normalized',...
        'backgroundcolor',[1 0.1 0.1], ...
        'callback',{@WelcomeScreenHomeDelivery,hndl.npanel}, ...
        'Units', 'normalized', 'Position',[0.885,0.025,0.1,0.052]);
    

    set(gcf, 'UserData', hndl);
end

